    class NameVerifier extends InputVerifier{

        @Override
        public boolean verify(JComponent input) {
            JTextField tf = (JTextField) input;
            return !tf.getText().isEmpty();
        }
        
    }
    
    class DescripcionVerifier extends InputVerifier {
        @Override
        public boolean verify(JComponent input) {
            JTextPane tp = (JTextPane) input;
            return !tp.getText().isEmpty();
        }
        
    }
    
    class PrecioVerifier extends InputVerifier {

        @Override
        public boolean verify(JComponent input) {
            JTextField tf = (JTextField) input;
            String text = tf.getText();
            if (!text.isEmpty()) {
                try {
                    float precio = Float.parseFloat(text);

                    if (precio > 0) {
                        return true;
                    }
                } catch (NumberFormatException ex) {
                    return false;
                }
            }
            return false;
        }
        
    }
    
