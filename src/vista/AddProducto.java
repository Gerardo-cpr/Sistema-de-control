    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (new NameVerifier().verify(nombreTf) && new DescripcionVerifier().verify(descripcionTp) && new PrecioVerifier().verify(precioTf)) {
            String nombre = nombreTf.getText();
            String descripcion = descripcionTp.getText();
            float precio = Float.parseFloat(precioTf.getText());
            int cantidad = (int)cantidadSp.getValue();
            ProductosDaoImpl accesoProductos = new ProductosDaoImpl();
            Producto productoAInsertar = new Producto(0, nombre, descripcion, precio, cantidad);
            
            ((MenuPrincipal) parent).recargarTabla(productoAInsertar, null, null);
            
            Thread thread = new Thread(){
                public void run(){
                    accesoProductos.insertarProducto(productoAInsertar);
                }
            };

            thread.start();
            
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Datos invalidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

